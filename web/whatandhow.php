<h3>7. Na čo je energia?</h3>

<p>
    Diggy potrebuje energiu pre kopanie políčok. Energia sa obnovuje rýchlosťou zodpovedajúcou rýchlosti regenerácie a to aj vtedy, ak je hráč offline. Maximálna kapacita a rýchlosť energie môže byť upravovaná pomocou výbavy kempu.
</p>

<p>
    Každý hráč začina s kapacitou 200. Ked ukazovateľ ukáže 200/200, kapacita je naplnená. Pomocou výbavy kempu sa toto číslo môže zvyšovať, napríklad na 300/300. Momentálna kapacita môže presiahnuť maximálnu v prápade, že hráč použije energetické jedlo alebo pitie (alebo klikne na božie dieťa alebo získa bonusovú energiu za dosiahnutie úrovne), napríklad na 350/300. V tomto prípade bude regenerácia pokračovať až keď momentálna energia klesne pod maximálnu, tj napríklad 299/300. V hre sa nachádzajú aj niektoré veľmi energeticky náročné políčka (potrebujú napríklad 600 energie) a najjednoduchší spôsob ako ich vykopať je práve pomocou prekročenia maximálnej kapacity energie.
</p>

